'''
Задание 1.
Найдите сумму цифр трехзначного числа n.
Результат сохраните в перменную res.

Пример:

n = 123 -> res = 6 (1 + 2 + 3)

n = 100 -> res = 1 (1 + 0 + 0)

Обязательно решите с применением f-строк

Для решения можно применять только операции деления
'''

# Задаём трёхзначное число
n = int(input("Введите трёхзначное число: "))

# Извлекаем цифры с помощью целочисленного деления и остатка
hundreds = n // 100           # сотни
tens     = (n // 10) % 10     # десятки
units    = n % 10             # единицы

# Суммируем цифры
res = hundreds + tens + units

# Выводим результат с помощью f-строки
print(f"Сумма цифр числа {n} равна {res}")