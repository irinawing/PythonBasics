'''
Задание 2.

В некоторой школе решили набрать три новых
математических класса и оборудовать кабинеты для
них новыми партами. За каждой партой может сидеть
два учащихся. Известно количество учащихся в
каждом из трех классов. Выведите наименьшее
число парт, которое нужно приобрести для них.

Input: 20 21 22(ввод чисел НЕ в одну строку)
Output: 32
'''

# Считываем количество учащихся в каждом из трёх классов (по одному числу на строку)
a = int(input("Введите число учащихся в первом классе: "))
b = int(input("Введите число учащихся во втором классе: "))
c = int(input("Введите число учащихся в третьем классе: "))

# Для каждого класса считаем, сколько парт нужно:
# за каждой партой могут сидеть двое, поэтому
# если учащихся нечётное число, последняя парта будет неполной
desks_a = (a + 1) // 2
desks_b = (b + 1) // 2
desks_c = (c + 1) // 2

# Суммируем количество парт
res = desks_a + desks_b + desks_c

# Выводим результат с помощью f-строки
print(f"{res}")
