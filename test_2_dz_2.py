"""
Задание 2.

Посчитайте сумму чётных элементов от 1 до n исключая кратные e.
Используйте while и if.
Попробуйте разные значения e и n.
"""

# Запрашиваем у пользователя границы n и e
n = int(input("Введите n (верхнюю границу): "))
e = int(input("Введите e (исключаемые кратные): "))

# Инициализируем сумму и счётчик
sum_even = 0
i = 1

# Проходим по всем числам от 1 до n
while i <= n:
    # Если число чётное и не кратно e — добавляем к сумме
    if i % 2 == 0 and i % e != 0:
        sum_even += i
    i += 1

# Выводим результат
print(f"Сумма чётных чисел от 1 до {n}, исключая кратные {e}, равна {sum_even}")