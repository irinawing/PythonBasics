"""
Задание 3.

Нарисовать в консоли ёлку спросив
у пользователя количество рядов.
Пример результата:
Сколько рядов у ёлки? 5
    *
   ***
  *****
 *******
*********
"""

# Запрашиваем у пользователя количество рядов ёлки
rows = int(input("Сколько рядов у ёлки? "))

# Для каждого ряда вычисляем количество пробелов и звёздочек
for i in range(rows):
    spaces = rows - i - 1            # число пробелов слева
    stars = 2 * i + 1                # число звёздочек в ряду
    # Выводим строку с пробелами и звёздочками
    print(f"{' ' * spaces}{'*' * stars}")