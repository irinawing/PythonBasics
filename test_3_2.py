"""
Задание 2.

Для списка реализовать обмен значений соседних элементов,
т.е. значениями обмениваются элементы с индексами 0 и 1, 2 и 3 и т.д.
При нечетном количестве элементов последний сохранить на своем месте.
Для заполнения списка элементов необходимо использовать функцию input().

Пример:
Введите целые числа через пробел: 1 2 3 4
Результат: 2 1 4 3

Введите целые числа через пробел: 1 2 3
Результат: 2 1 3
"""

def swap_neighbors(lst):
    """
    Меняет местами элементы с индексами 0 и 1, 2 и 3 и т.д.
    При нечетном количестве элементов последний остается на месте.
    """
    for i in range(0, len(lst) - 1, 2):
        lst[i], lst[i + 1] = lst[i + 1], lst[i]
    return lst

def main():
    # Чтение строки чисел и преобразование в список целых
    data = input("Введите целые числа через пробел: ")
    try:
        numbers = [int(x) for x in data.split()]
    except ValueError:
        print("Пожалуйста, вводите только целые числа.")
        return

    # Обмен соседних элементов
    result = swap_neighbors(numbers)

    # Вывод результата
    print("Результат:", *result)

if __name__ == "__main__":
    main()