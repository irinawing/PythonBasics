'''Задание 1
1. Реализовать функцию, принимающую два числа (позиционные аргументы)
и выполняющую их деление. Числа запрашивать у пользователя,
предусмотреть обработку ситуации деления на ноль (try except).

Пример:
Введите первое число: 4
Введите второе число: 4
1.0

Введите первое число: 4
Введите второе число: 0
Пытаетесь делить на 0!

Введите первое число: 4
Введите второе число: g
Пожалуйста, вводите только числа
'''

def divide_numbers():
    """
    Запрашивает у пользователя два числа и выводит результат их деления.
    Обрабатывает ошибки деления на ноль и некорректного ввода.
    """
    try:
        a = float(input("Введите первое число: "))
        b = float(input("Введите второе число: "))
        result = a / b
    except ZeroDivisionError:
        print("Пытаетесь делить на 0!")
        return
    except ValueError:
        print("Пожалуйста, вводите только числа")
        return

    print(f"{result}")

if __name__ == "__main__":
    divide_numbers()