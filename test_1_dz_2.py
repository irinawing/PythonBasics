'''
Задание 2.

За день машина проезжает n километров. Сколько
дней нужно, чтобы проехать маршрут длиной m
километров? При решении этой задачи нельзя
пользоваться условной инструкцией if и циклами.

Input:
n = 700
m = 750
Output:
2
'''

# Считываем данные
n = int(input("Введите, сколько километров машина проезжает за день (n): "))
m = int(input("Введите длину маршрута в километрах (m): "))

# Вычисляем минимальное число дней без использования if и циклов
days = (m + n - 1) // n

# Выводим результат с помощью f-строки
print(f"{days}")
